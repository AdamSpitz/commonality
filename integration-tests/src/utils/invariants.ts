/**
 * Execute a GraphQL query.
 * Accepts either a TypedDocumentNode (generated by codegen) or a raw query string.
 * When passed a TypedDocumentNode, the result type TData is inferred automatically.
 */
async function executeTypedGraphQLQuery<TData = any, TVars extends Record<string, any> = Record<string, any>>(
  machinery: SDKMachinery,
  document: TypedDocumentNode<TData, TVars> | string,
  variables?: TVars
): Promise<TData> {
  const queryString = typeof document === 'string' ? document : print(document);
  const response = await fetch(machinery.indexerUrl, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      query: queryString,
      variables,
    }),
  });

  if (!response.ok) {
    throw new Error(`GraphQL request failed: ${response.status} ${response.statusText}`);
  }

  const result = await response.json() as { data?: TData; errors?: any[] };

  if (result.errors) {
    throw new Error(`GraphQL errors: ${JSON.stringify(result.errors)}`);
  }

  return result.data as TData;
}

