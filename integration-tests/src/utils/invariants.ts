/**
 * State Consistency Invariant #5: Orphaned data check
 *
 * Section 1 from generative-test-prep.md
 *
 * Verifies that all entity references are valid - i.e., every child entity that references
 * a parent entity has a valid parent that exists in the database. This checks referential
 * integrity across the indexer's data model.
 *
 * Checks performed:
 * - Belief records reference existing Statements and Users
 * - Implication records reference existing Statements (from/to) and Attesters
 * - Project-related records (Contributions, Refunds, etc.) reference existing Projects
 * - Delegation-related records reference existing DelegatableNotes
 * - Cross-subsystem references (DelegatableNote -> Statement, ProjectAlignment -> Project/Statement)
 *
 * This is a fundamental data integrity check that should always pass. If it fails,
 * it indicates a bug in the indexer's event handling logic.
 *
 * Note: This invariant can be expensive to run on large datasets since it queries
 * potentially many entities. Consider running it selectively or with sampling in
 * production generative tests.
 *
 * @param graphqlClient GraphQL client or executor
 */
export async function assertNoOrphanedData(
  machinery: ActionTestingMachinery
): Promise<void> {
  // Check Concept Space subsystem
  await checkOrphanedBeliefs(machinery);
  await checkOrphanedImplications(machinery);
}

/**
 * Check that all Belief records reference valid Statements and Users
 */
async function checkOrphanedBeliefs(
  machinery: ActionTestingMachinery
): Promise<void> {
  // Get all beliefs with a non-zero belief state (active beliefs/disbeliefs)
  const beliefsResult = await executeTypedGraphQLQuery(
    machinery,
    InvariantGetAllActiveBeliefsDocument
  );

  const beliefs = beliefsResult.beliefss?.items || [];

  // Check each belief references a valid statement
  const checkedStatements = new Set<string>();
  const checkedUsers = new Set<string>();

  for (const belief of beliefs) {
    const statementId = belief.statementId.toLowerCase();
    // user is nullable in the schema; skip orphaned beliefs missing a user entity
    if (!belief.user) continue;
    const userId = belief.user.id.toLowerCase();

    // Check statement exists (cache checks to avoid redundant queries)
    if (!checkedStatements.has(statementId)) {
      const statementResult = await executeTypedGraphQLQuery(
        machinery,
        InvariantCheckStatementExistsDocument,
        { id: statementId }
      );

      if (!statementResult.statements) {
        throw new Error(
          `Orphaned Belief: User ${userId} has a belief record for statement ${statementId}, ` +
          `but that statement does not exist in the database. ` +
          `This indicates a referential integrity violation in the indexer.`
        );
      }

      checkedStatements.add(statementId);
    }

    // Check user exists (cache checks to avoid redundant queries)
    if (!checkedUsers.has(userId)) {
      const userResult = await executeTypedGraphQLQuery(
        machinery,
        InvariantCheckUserExistsDocument,
        { id: userId }
      );

      if (!userResult.users) {
        throw new Error(
          `Orphaned Belief: User ${userId} has belief records, ` +
          `but does not exist as a User entity in the database. ` +
          `This indicates a referential integrity violation in the indexer.`
        );
      }

      checkedUsers.add(userId);
    }
  }
}

/**
 * Check that all Implication records reference valid Statements (from/to) and Attesters
 */
async function checkOrphanedImplications(
  machinery: ActionTestingMachinery
): Promise<void> {
  // Get all implications
  const implicationsResult = await executeTypedGraphQLQuery(
    machinery,
    InvariantGetAllImplicationsDocument
  );

  const implications = implicationsResult.implicationss?.items || [];

  // Check each implication references valid entities
  const checkedStatements = new Set<string>();
  const checkedAttesters = new Set<string>();

  for (const implication of implications) {
    const fromId = implication.fromStatementCid.toLowerCase();
    const toId = implication.toStatementCid.toLowerCase();
    // attester is nullable in the schema; skip implications missing one (shouldn't happen in practice)
    if (!implication.attester) continue;
    const attesterId = implication.attester.id.toLowerCase();

    // Check fromStatement exists
    if (!checkedStatements.has(fromId)) {
      const statementResult = await executeTypedGraphQLQuery(
        machinery,
        InvariantCheckStatementExistsDocument,
        { id: fromId }
      );

      if (!statementResult.statements) {
        throw new Error(
          `Orphaned Implication: Attester ${attesterId} attested ${fromId}→${toId}, ` +
          `but source statement ${fromId} does not exist in the database. ` +
          `This indicates a referential integrity violation in the indexer.`
        );
      }

      checkedStatements.add(fromId);
    }

    // Check toStatement exists
    if (!checkedStatements.has(toId)) {
      const statementResult = await executeTypedGraphQLQuery(
        machinery,
        InvariantCheckStatementExistsDocument,
        { id: toId }
      );

      if (!statementResult.statements) {
        throw new Error(
          `Orphaned Implication: Attester ${attesterId} attested ${fromId}→${toId}, ` +
          `but target statement ${toId} does not exist in the database. ` +
          `This indicates a referential integrity violation in the indexer.`
        );
      }

      checkedStatements.add(toId);
    }

    // Check attester exists
    if (!checkedAttesters.has(attesterId)) {
      const attesterResult = await executeTypedGraphQLQuery(
        machinery,
        InvariantCheckAttesterExistsDocument,
        { id: attesterId }
      );

      if (!attesterResult.attesters) {
        throw new Error(
          `Orphaned Implication: Implication ${fromId}→${toId} references attester ${attesterId}, ` +
          `but that attester does not exist in the database. ` +
          `This indicates a referential integrity violation in the indexer.`
        );
      }

      checkedAttesters.add(attesterId);
    }
  }
}

/**
 * Business Logic Constraint: Unique statements (CID-based deduplication)
 *
 * Section 4 from generative-test-prep.md
 *
 * Verifies that statements with identical IPFS content have the same statementId.
 * This checks that the CID-based deduplication is working correctly.
 *
 * In the Commonality system, statement IDs are derived from IPFS content identifiers (CIDs).
 * Two statements with identical content should produce the same CID, and therefore should
 * have the same statementId in the database. This function verifies this property by:
 *
 * 1. Taking two statementIds that are expected to represent identical content
 * 2. Verifying they are actually the same ID (i.e., deduplication occurred)
 *
 * This is primarily a test helper to verify deduplication behavior when creating statements.
 * It's typically called after attempting to create a "duplicate" statement to verify that
 * the system correctly identified it as a duplicate and returned the existing statement's ID.
 *
 * @param statementId1 First statement ID (expected to be identical to statementId2)
 * @param statementId2 Second statement ID (expected to be identical to statementId1)
 * @param context Optional context string for error messages (e.g., "after creating statement with same content")
 */
export async function assertUniqueStatements(
  statementId1: string,
  statementId2: string,
  context?: string
): Promise<void> {
  const contextMsg = context ? ` ${context}` : '';

  assert.strictEqual(
    statementId1.toLowerCase(),
    statementId2.toLowerCase(),
    `Statement uniqueness violation${contextMsg}. ` +
    `Two statements with identical IPFS content should have the same statementId (CID-based deduplication). ` +
    `Got ${statementId1} and ${statementId2}. ` +
    `This indicates that either the CID calculation is non-deterministic or deduplication is not working correctly.`
  );
}

/**
 * Temporal/Historical Property: Monotonic project funding
 *
 * Section 6 from generative-test-prep.md
 *
 * Verifies that a project's totalReceived amount never decreases between two snapshots
 * (unless a refund occurred, in which case the decrease should match the refund amount).
 *
 * This is a temporal property that helps catch bugs in the indexer's event handling.
 * The totalReceived should only ever increase (when contributions are made) or decrease
 * (when refunds are processed). It should never spontaneously change or decrease without
 * a corresponding blockchain event.
 *
 * This function is typically called before and after a sequence of actions to verify that
 * the funding behaves correctly over time.
 *
 * @param graphqlClient GraphQL client or executor
 * @param projectAddress The project's assurance contract address
 * @param expectedBefore The totalReceived value from a previous snapshot
 * @param allowRefunds If true, allows totalReceived to decrease (for tests that include refunds)
 */
export async function assertMonotonicProjectFunding(
  machinery: ActionTestingMachinery,
  projectAddress: string,
  expectedBefore: bigint,
  allowRefunds: boolean = false
): Promise<void> {
  // Get the current project state
  const project = await getProject(machinery, projectAddress.toLowerCase());

  if (!project) {
    throw new Error(`Project ${projectAddress} not found`);
  }

  const currentTotal = BigInt(project.totalReceived);

  if (allowRefunds) {
    // With refunds allowed, we just verify the value is valid (not negative)
    assert(
      currentTotal >= 0n,
      `Project ${projectAddress}: totalReceived became negative: ${currentTotal.toString()}`
    );
  } else {
    // Without refunds, totalReceived should never decrease
    assert(
      currentTotal >= expectedBefore,
      `Project ${projectAddress}: totalReceived decreased without refunds. ` +
      `Expected at least ${expectedBefore.toString()}, got ${currentTotal.toString()}. ` +
      `This violates the monotonic funding property - totalReceived should only increase ` +
      `unless refunds are processed.`
    );
  }
}

/**
 * Business Logic Constraint: Assurance contract refund eligibility
 *
 * Section 4 from generative-test-prep.md
 *
 * Verifies that the refund eligibility rules for assurance contracts are correctly enforced:
 *
 * Refunds ARE allowed when:
 * - The deadline has passed AND the project failed to meet its funding threshold
 *
 * Refunds are NOT allowed when:
 * - The deadline has not passed yet (project fate still undecided)
 * - The deadline has passed AND the project met its threshold (project succeeded)
 *
 * This checks that the business logic constraint matches what the smart contract enforces
 * in AssuranceContract.sol's requireAssuranceContractHasFailed() function.
 *
 * Note: This is a business logic check that verifies the indexer correctly reflects the
 * smart contract's refund rules. The actual refund permission enforcement happens on-chain,
 * but we verify that the indexer's view of project status (totalReceived, threshold, deadline)
 * is consistent with refund eligibility.
 *
 * @param graphqlClient GraphQL client or executor
 * @param projectAddress The project's assurance contract address
 * @param currentBlockTimestamp The current blockchain timestamp (from latest block)
 * @param shouldAllowRefunds Whether refunds should currently be allowed based on contract state
 */
export async function assertAssuranceContractRefundLogic(
  machinery: ActionTestingMachinery,
  projectAddress: string,
  currentBlockTimestamp: bigint,
  shouldAllowRefunds?: boolean
): Promise<void> {
  // Get the project state
  const project = await getProject(machinery, projectAddress.toLowerCase());

  if (!project) {
    throw new Error(`Project ${projectAddress} not found`);
  }

  const totalReceived = BigInt(project.totalReceived);
  const threshold = BigInt(project.threshold);
  const deadline = BigInt(project.deadline);

  // If this project uses a non-EthThreshold condition, threshold and deadline are both 0n
  // (fallback values from on-chain reads). The threshold/deadline-based refund logic doesn't
  // apply in that case, so we skip this invariant check.
  if (threshold === 0n && deadline === 0n) {
    return;
  }

  // Determine if refunds should be allowed based on the business rules
  const deadlineHasPassed = currentBlockTimestamp >= deadline;
  const thresholdWasMet = totalReceived >= threshold;

  // Refunds are allowed if: deadline passed AND threshold NOT met
  const refundsAllowed = deadlineHasPassed && !thresholdWasMet;

  // If the caller provided an expected value, verify it matches our calculation
  if (shouldAllowRefunds !== undefined) {
    assert.strictEqual(
      refundsAllowed,
      shouldAllowRefunds,
      `Project ${projectAddress}: Refund eligibility mismatch. ` +
      `Expected refunds ${shouldAllowRefunds ? 'allowed' : 'not allowed'}, ` +
      `but calculated ${refundsAllowed ? 'allowed' : 'not allowed'}. ` +
      `State: totalReceived=${totalReceived.toString()}, threshold=${threshold.toString()}, ` +
      `deadline=${deadline.toString()}, currentTime=${currentBlockTimestamp.toString()}`
    );
  }

  // Also verify the logical consistency of the state
  // If deadline hasn't passed, refunds must not be allowed
  if (!deadlineHasPassed) {
    assert(
      !refundsAllowed,
      `Project ${projectAddress}: Refunds are allowed before deadline. ` +
      `This violates the business rule that refunds require deadline to pass. ` +
      `Deadline: ${deadline.toString()}, CurrentTime: ${currentBlockTimestamp.toString()}`
    );
  }

  // If threshold was met and deadline passed, refunds must not be allowed
  if (deadlineHasPassed && thresholdWasMet) {
    assert(
      !refundsAllowed,
      `Project ${projectAddress}: Refunds are allowed for successful project. ` +
      `This violates the business rule that successful projects cannot be refunded. ` +
      `TotalReceived: ${totalReceived.toString()}, Threshold: ${threshold.toString()}`
    );
  }

  // If deadline passed and threshold was NOT met, refunds must be allowed
  if (deadlineHasPassed && !thresholdWasMet) {
    assert(
      refundsAllowed,
      `Project ${projectAddress}: Refunds are not allowed for failed project. ` +
      `This violates the business rule that failed projects must allow refunds. ` +
      `TotalReceived: ${totalReceived.toString()}, Threshold: ${threshold.toString()}, ` +
      `Deadline: ${deadline.toString()}, CurrentTime: ${currentBlockTimestamp.toString()}`
    );
  }
}

/**
 * Query Consistency Check: Generic aggregated count vs actual records
 *
 * Section 3 from generative-test-prep.md
 *
 * This is a generic version of the query consistency pattern used throughout the system.
 * It verifies that cached/aggregated counts on parent entities match the actual number
 * of child records when queried directly.
 *
 * This pattern applies to many entity relationships in the system:
 * - Statement.believerCount vs count of Belief records where beliefState=BELIEVES
 * - Statement.disbelieverCount vs count of Belief records where beliefState=DISBELIEVES
 * - Project.contributorCount vs count of distinct contributors in Contribution records
 * - Attester.implicationCount vs count of Implication records
 * - etc.
 *
 * The function takes two queries:
 * 1. A query that returns the cached/aggregated count from a parent entity
 * 2. A query that returns the actual child records to count
 *
 * And verifies that they match. This catches bugs where the indexer's aggregation logic
 * doesn't correctly maintain denormalized counts.
 *
 * @param graphqlClient GraphQL client or executor
 * @param countQuery GraphQL query that returns the cached count (must return single number field)
 * @param countQueryVariables Variables for the count query
 * @param countFieldPath Path to extract count from result (e.g., "statement.believerCount")
 * @param recordsQuery GraphQL query that returns the actual records to count
 * @param recordsQueryVariables Variables for the records query
 * @param recordsFieldPath Path to extract array from result (e.g., "beliefss.items")
 * @param entityDescription Description for error messages (e.g., "Statement X's believerCount")
 */
export async function assertAggregatedCountConsistency(
  machinery: ActionTestingMachinery,
  countQuery: TypedDocumentNode<any, any> | string,
  countQueryVariables: Record<string, any>,
  countFieldPath: string,
  recordsQuery: TypedDocumentNode<any, any> | string,
  recordsQueryVariables: Record<string, any>,
  recordsFieldPath: string,
  entityDescription: string
): Promise<void> {
  const countResult = await executeTypedGraphQLQuery(
    machinery,
    countQuery,
    countQueryVariables
  );

  // Extract the count value using the field path
  const cachedCount = extractFieldByPath(countResult, countFieldPath);
  if (typeof cachedCount !== 'number') {
    throw new Error(
      `Count query for ${entityDescription} did not return a number. ` +
      `Field path "${countFieldPath}" resolved to: ${JSON.stringify(cachedCount)}`
    );
  }

  // Execute the records query
  const recordsResult = await executeTypedGraphQLQuery(
    machinery,
    recordsQuery,
    recordsQueryVariables
  );

  // Extract the records array using the field path
  const records = extractFieldByPath(recordsResult, recordsFieldPath);
  if (!Array.isArray(records)) {
    throw new Error(
      `Records query for ${entityDescription} did not return an array. ` +
      `Field path "${recordsFieldPath}" resolved to: ${JSON.stringify(records)}`
    );
  }

  const actualCount = records.length;

  // Verify the counts match
  assert.strictEqual(
    cachedCount,
    actualCount,
    `${entityDescription}: Aggregated count mismatch. ` +
    `Cached count (${countFieldPath}) is ${cachedCount}, ` +
    `but actual record count (${recordsFieldPath}) is ${actualCount}. ` +
    `This indicates the indexer's denormalized count is out of sync with the actual records.`
  );
}

/**
 * Helper function to extract a nested field from an object using a dot-separated path
 * Example: extractFieldByPath({a: {b: {c: 42}}}, "a.b.c") returns 42
 */
function extractFieldByPath(obj: any, path: string): any {
  const parts = path.split('.');
  let current = obj;

  for (const part of parts) {
    if (current == null) {
      return undefined;
    }
    current = current[part];
  }

  return current;
}

/**
 * State Consistency Invariant: Implication bidirectionality
 *
 * Section 1 from generative-test-prep.md
 *
 * Verifies that the indexer's view of implications is consistent with on-chain reality.
 * If the indexer shows an implication S1→S2 attested by address A, then this implication
 * should be well-formed and internally consistent.
 *
 * This is a data integrity check that ensures the indexer correctly stores and retrieves
 * implication data. It verifies that:
 * - The implication exists in the indexer
 * - All required fields are populated (not null/empty)
 * - The fields match the query parameters (fromStatement, toStatement, attester)
 *
 * Note: In integration tests, the indexer is fed by blockchain events, so the presence
 * of an implication in the indexer implies that the corresponding ImplicationAttested
 * event was emitted and processed. This function verifies the indexer's data integrity
 * rather than querying the blockchain directly (which would be more expensive).
 *
 * @param graphqlClient GraphQL client or executor
 * @param fromStatementCid The source statement ID
 * @param toStatementCid The target statement ID
 * @param attesterAddress The attester's address
 */
export async function assertImplicationBidirectionality(
  machinery: ActionTestingMachinery,
  fromStatementCid: IpfsCidV1,
  toStatementCid: IpfsCidV1,
  attesterAddress: string
): Promise<void> {
  const normalizedAttester = attesterAddress.toLowerCase();

  // Query the indexer for implications from this statement
  const implications = await getImplicationsFrom(machinery, fromStatementCid);

  const implication = implications.find(
    (imp) =>
      imp.toStatementCid === toStatementCid &&
      ((imp.attester as any).id || imp.attester).toLowerCase() === normalizedAttester
  );

  if (!implication) {
    throw new Error(
      `Implication ${fromStatementCid}→${toStatementCid} by ${attesterAddress} not found in indexer. ` +
      `Cannot verify bidirectionality. This function should only be called for implications ` +
      `that are expected to exist.`
    );
  }

  // Verify the implication data is well-formed
  assert.ok(
    implication.fromStatementCid,
    `Implication ${fromStatementCid}→${toStatementCid}: fromStatementCid should not be null/empty`
  );

  assert.ok(
    implication.toStatementCid,
    `Implication ${fromStatementCid}→${toStatementCid}: toStatementCid should not be null/empty`
  );

  assert.ok(
    implication.attester,
    `Implication ${fromStatementCid}→${toStatementCid}: attester should not be null/empty`
  );

  // Verify the IDs match what we queried for (data integrity check)
  assert.strictEqual(
    implication.fromStatementCid,
    fromStatementCid,
    `Implication fromStatementCid mismatch. Expected ${fromStatementCid}, ` +
    `got ${implication.fromStatementCid}`
  );

  assert.strictEqual(
    implication.toStatementCid,
    toStatementCid,
    `Implication toStatementCid mismatch. Expected ${toStatementCid}, ` +
    `got ${implication.toStatementCid}`
  );

  const attesterIdFromResponse = (implication.attester as any).id || implication.attester;
  assert.strictEqual(
    attesterIdFromResponse.toLowerCase(),
    normalizedAttester,
    `Implication attester mismatch. Expected ${attesterAddress}, ` +
    `got ${attesterIdFromResponse}`
  );
}

/**
 * Business Logic Constraint: Implication non-transitivity
 *
 * Section 4 from generative-test-prep.md
 *
 * Verifies that the system correctly implements non-transitive implication semantics.
 * Just because S1→S2 and S2→S3 exist doesn't mean the system should automatically
 * show S1's believers as indirect supporters of S3. There must be a DIRECT attestation
 * of S1→S3 for that support to propagate.
 *
 * This is a fundamental business rule that prevents unintended support propagation
 * through long implication chains. For example:
 * - Alice believes "I support climate action" (S1)
 * - Attester says S1→"I support carbon taxes" (S2)
 * - Attester says S2→"I support $500/ton carbon tax" (S3)
 *
 * Alice should appear as an indirect supporter of S2 (one hop), but NOT S3 (two hops),
 * unless there's a direct S1→S3 attestation.
 *
 * This function verifies the non-transitivity property by:
 * 1. Checking that S1→S2 and S2→S3 implications exist
 * 2. Verifying that NO S1→S3 implication exists
 * 3. Confirming that believers of S1 appear as indirect supporters of S2
 * 4. Confirming that believers of S1 do NOT appear as indirect supporters of S3
 *
 * @param graphqlClient GraphQL client or executor
 * @param s1Id Statement ID for the first statement (source)
 * @param s2Id Statement ID for the middle statement
 * @param s3Id Statement ID for the third statement (target)
 * @param attesterAddress Address of the attester who created the implications
 * @param believerAddress Address of a user who believes S1 (for verification)
 */
export async function assertImplicationNonTransitivity(
  machinery: ActionTestingMachinery,
  s1Cid: IpfsCidV1,
  s2Cid: IpfsCidV1,
  s3Cid: IpfsCidV1,
  attesterAddress: string,
  believerAddress: string
): Promise<void> {
  // Import SDK constants and query functions dynamically to avoid circular dependencies
  const { BELIEVES, DISBELIEVES } = await import('@commonality/sdk');
  const normalizedAttester = attesterAddress.toLowerCase();
  const normalizedBeliever = believerAddress.toLowerCase();

  // Step 1: Verify S1→S2 implication exists
  const implicationsFromS1 = await getImplicationsFrom(machinery, s1Cid);
  const s1ToS2 = implicationsFromS1.find(
    (imp) =>
      imp.toStatementCid === s2Cid &&
      ((imp.attester as any).id || imp.attester).toLowerCase() === normalizedAttester
  );

  assert.ok(
    s1ToS2,
    `Non-transitivity test setup error: S1→S2 implication should exist. ` +
    `Attester ${attesterAddress} has not attested ${s1Cid}→${s2Cid}`
  );

  // Step 2: Verify S2→S3 implication exists
  const implicationsFromS2 = await getImplicationsFrom(machinery, s2Cid);
  const s2ToS3 = implicationsFromS2.find(
    (imp) =>
      imp.toStatementCid === s3Cid &&
      ((imp.attester as any).id || imp.attester).toLowerCase() === normalizedAttester
  );

  assert.ok(
    s2ToS3,
    `Non-transitivity test setup error: S2→S3 implication should exist. ` +
    `Attester ${attesterAddress} has not attested ${s2Cid}→${s3Cid}`
  );

  // Step 3: Verify NO S1→S3 implication exists (critical for this test)
  const s1ToS3 = implicationsFromS1.find(
    (imp) =>
      imp.toStatementCid === s3Cid &&
      ((imp.attester as any).id || imp.attester).toLowerCase() === normalizedAttester
  );

  assert.ok(
    !s1ToS3,
    `Non-transitivity test setup error: S1→S3 implication should NOT exist. ` +
    `Found unexpected direct attestation ${s1Cid}→${s3Cid} by ${attesterAddress}. ` +
    `This test requires S1→S2 and S2→S3 to exist WITHOUT a direct S1→S3.`
  );

  // Step 4: Verify the believer believes S1
  const believerS1Belief = await getUserBelief(machinery, normalizedBeliever, s1Cid);
  assert.strictEqual(
    believerS1Belief?.beliefState,
    BELIEVES,
    `Non-transitivity test setup error: User ${believerAddress} should believe S1 (${s1Cid}). ` +
    `Got beliefState: ${believerS1Belief?.beliefState}`
  );

  // Step 5: Verify the believer does NOT explicitly disbelieve S2 or S3
  // (If they disbelieve, they won't appear as indirect supporters regardless)
  const believerS2Belief = await getUserBelief(machinery, normalizedBeliever, s2Cid);
  assert.notStrictEqual(
    believerS2Belief?.beliefState,
    DISBELIEVES,
    `Non-transitivity test setup error: User ${believerAddress} should NOT disbelieve S2 (${s2Cid}). ` +
    `This would prevent them from appearing as indirect supporter.`
  );

  const believerS3Belief = await getUserBelief(machinery, normalizedBeliever, s3Cid);
  assert.notStrictEqual(
    believerS3Belief?.beliefState,
    DISBELIEVES,
    `Non-transitivity test setup error: User ${believerAddress} should NOT disbelieve S3 (${s3Cid}). ` +
    `This would prevent them from appearing as indirect supporter.`
  );

  // Step 6: Verify believer appears as indirect supporter of S2 (one hop: S1→S2)
  const s2IndirectSupporters = await getIndirectSupporters(machinery, s2Cid, normalizedAttester);
  const s2SupporterAddresses = s2IndirectSupporters.map(s => s.user.toLowerCase());

  assert.ok(
    s2SupporterAddresses.includes(normalizedBeliever),
    `Implication transitivity violation: User ${believerAddress} believes S1 (${s1Cid}), ` +
    `and S1→S2 exists, so they should appear as indirect supporter of S2 (${s2Cid}). ` +
    `Found ${s2IndirectSupporters.length} indirect supporters, ` +
    `but ${believerAddress} is not among them: [${s2SupporterAddresses.join(', ')}]`
  );

  // Step 7: Verify believer does NOT appear as indirect supporter of S3 (two hops: S1→S2→S3)
  // This is the CORE non-transitivity check
  const s3IndirectSupporters = await getIndirectSupporters(machinery, s3Cid, normalizedAttester);
  const s3SupporterAddresses = s3IndirectSupporters.map(s => s.user.toLowerCase());

  assert.ok(
    !s3SupporterAddresses.includes(normalizedBeliever),
    `Implication NON-transitivity violation: User ${believerAddress} believes S1 (${s1Cid}), ` +
    `and we have S1→S2 and S2→S3 (but NO direct S1→S3), so they should NOT appear ` +
    `as indirect supporter of S3 (${s3Cid}). The system incorrectly propagated support ` +
    `through TWO hops. This violates the business rule that implications are non-transitive. ` +
    `Found ${s3IndirectSupporters.length} indirect supporters of S3: [${s3SupporterAddresses.join(', ')}], ` +
    `and ${believerAddress} should NOT be in this list.`
  );
}

