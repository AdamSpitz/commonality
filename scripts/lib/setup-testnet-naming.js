import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { spawnSync } from 'node:child_process';
import { createInterface } from 'node:readline';
import * as Name from 'w3name';

const root = path.resolve(path.dirname(new URL(import.meta.url).pathname), '../..');
const manifestPath = path.join(root, 'deployments/testnet-names.json');
const secretsPath = path.join(root, '.env.secrets');
const ipnsInventoryPath = path.join(root, 'deployments/testnet-ipns.env');

const args = new Set(process.argv.slice(2));
if (args.has('--help')) {
  console.log(`Usage: ./scripts/setup-testnet-naming.sh [--dns] [--ens] [--uber] [--uber-only] [--yes]\n\nDefault mode is safe/offline: create or reuse IPNS keys in .env.secrets\nand write the public IPNS inventory to deployments/testnet-ipns.env. This\nincludes both per-app IPNS names and the root uber-UI IPNS name used by\ncommonality.eth.\n\nUI URL defaults are non-secret deployment config; keep them in\ndeployments/base-sepolia.env, not .env.secrets.\n\nOptional modes:\n  --dns        upsert Cloudflare CNAME and DNSLink TXT records for per-app UI\n               domains. Requires CLOUDFLARE_API_TOKEN and CLOUDFLARE_ZONE_ID\n               in .env.secrets.\n  --ens        call scripts/update-ens.sh for each per-app UI name. Requires\n               the ENS subdomains/resolvers to already exist and\n               ENS_OWNER_PRIVATE_KEY.\n  --uber       with --ens, also set the root commonality.eth contenthash to the\n               uber-UI IPNS name. Offline key generation always includes this\n               key.\n  --uber-only  with --ens --uber, update only commonality.eth (not per-app ENS).\n  --yes        do not prompt before network-changing --dns/--ens operations.\n`);
  process.exit(0);
}

const doDns = args.has('--dns');
const doEns = args.has('--ens');
const doUberEns = args.has('--uber');
const doUberOnly = args.has('--uber-only');
const doAppEns = doEns && !doUberOnly;
const yes = args.has('--yes');

function readEnv(file) {
  if (!fs.existsSync(file)) return new Map();
  const out = new Map();
  for (const line of fs.readFileSync(file, 'utf8').split(/\r?\n/)) {
    const match = line.match(/^([A-Za-z_][A-Za-z0-9_]*)=(.*)$/);
    if (match) out.set(match[1], match[2]);
  }
  return out;
}

function appendEnv(entries) {
  if (entries.length === 0) return;
  const prefix = fs.existsSync(secretsPath) ? '\n' : '';
  fs.appendFileSync(secretsPath, `${prefix}# Added by scripts/setup-testnet-naming.sh\n${entries.map(([k, v]) => `${k}=${v}`).join('\n')}\n`);
}

async function requireConfirm(message) {
  if (yes) return;
  return new Promise((resolve, reject) => {
    const rl = createInterface({ input: process.stdin, output: process.stdout });
    rl.question(`${message} Type yes to continue: `, (answer) => {
      rl.close();
      if (answer.trim() !== 'yes') reject(new Error('Aborted.'));
      else resolve();
    });
  });
}

const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
if (!fs.existsSync(secretsPath)) fs.writeFileSync(secretsPath, '# Commonality private deployment secrets\n');
let env = readEnv(secretsPath);
const additions = [];

async function getOrCreateName(envVar, legacyEnvVar) {
  let privateKey = env.get(envVar) ?? (legacyEnvVar ? env.get(legacyEnvVar) : undefined);
  let name;
  if (privateKey) {
    name = await Name.from(Buffer.from(privateKey, 'base64'));
    if (!env.has(envVar)) additions.push([envVar, privateKey]);
  } else {
    name = await Name.create();
    privateKey = Buffer.from(name.key.raw).toString('base64');
    additions.push([envVar, privateKey]);
  }
  return { privateKey, ipnsName: name.toString() };
}

const rows = [];
for (const domain of manifest.domains) {
  const { ipnsName } = await getOrCreateName(domain.envVar, domain.legacyEnvVar);
  rows.push({
    slug: domain.slug,
    envVar: domain.envVar,
    ipnsName,
    ensName: `${domain.slug}.testnet.${manifest.ensRoot}`,
    dnsName: `${domain.slug}.testnet.${manifest.dnsRoot}`,
  });
}

let uberRow;
if (manifest.uberUi) {
  const { ipnsName } = await getOrCreateName(manifest.uberUi.envVar);
  uberRow = {
    slug: 'uber-ui',
    envVar: manifest.uberUi.envVar,
    ipnsNameEnvVar: manifest.uberUi.ipnsNameEnvVar ?? 'IPNS_NAME_TESTNET_UBER_UI',
    ipnsName,
    ensName: manifest.uberUi.ensName,
  };
}

appendEnv(additions);
env = readEnv(secretsPath);

fs.writeFileSync(ipnsInventoryPath, [
  '# Non-secret testnet IPNS inventory. Generated by scripts/setup-testnet-naming.sh',
  ...rows.map((r) => `IPNS_NAME_TESTNET_${r.slug.toUpperCase().replaceAll('-', '_')}=${r.ipnsName}`),
  ...(uberRow ? [uberRow.ipnsNameEnvVar ? `${uberRow.ipnsNameEnvVar}=${uberRow.ipnsName}` : `IPNS_NAME_TESTNET_UBER_UI=${uberRow.ipnsName}`] : []),
  '',
].join('\n'));
console.log(`Wrote ${ipnsInventoryPath}`);
console.log(additions.length ? `Appended ${additions.length} missing IPNS key values to .env.secrets` : '.env.secrets already had all generated IPNS key values');

async function cf(pathSuffix, options = {}) {
  const token = env.get('CLOUDFLARE_API_TOKEN');
  const res = await fetch(`https://api.cloudflare.com/client/v4${pathSuffix}`, {
    ...options,
    headers: { Authorization: `Bearer ${token}`, 'Content-Type': 'application/json', ...(options.headers ?? {}) },
  });
  const body = await res.json();
  if (!body.success) throw new Error(`Cloudflare API failed: ${JSON.stringify(body.errors)}`);
  return body.result;
}

if (doDns) {
  await requireConfirm(`This will upsert Cloudflare DNS records for ${manifest.dnsRoot}.`);
  const token = env.get('CLOUDFLARE_API_TOKEN');
  const zone = env.get('CLOUDFLARE_ZONE_ID');
  if (!token || !zone) throw new Error('CLOUDFLARE_API_TOKEN and CLOUDFLARE_ZONE_ID are required in .env.secrets for --dns.');
  for (const r of rows) {
    for (const rec of [
      { type: 'CNAME', name: r.dnsName, content: 'cloudflare-ipfs.com', proxied: false },
      { type: 'TXT', name: `_dnslink.${r.dnsName}`, content: `dnslink=/ipns/${r.ipnsName}` },
    ]) {
      const found = await cf(`/zones/${zone}/dns_records?type=${rec.type}&name=${encodeURIComponent(rec.name)}`);
      const method = found.length ? 'PUT' : 'POST';
      const url = found.length ? `/zones/${zone}/dns_records/${found[0].id}` : `/zones/${zone}/dns_records`;
      await cf(url, { method, body: JSON.stringify({ ttl: 1, ...rec }) });
      console.log(`${found.length ? 'Updated' : 'Created'} ${rec.type} ${rec.name}`);
    }
  }
}

if (doEns) {
  if (doUberOnly && !doUberEns) throw new Error('--uber-only requires --uber.');
  const appCount = doAppEns ? rows.length : 0;
  const uberCount = doUberEns && uberRow ? 1 : 0;
  console.log(`Will set ENS contenthashes for ${appCount + uberCount} names${appCount && uberCount ? ` (${appCount} per-app + root uber-UI)` : ''} on Ethereum mainnet. Each will be confirmed individually.\n`);
  for (const r of doAppEns ? rows : []) {
    await requireConfirm(`Submit ENS contenthash transaction for ${r.ensName}?`);
    const result = spawnSync(path.join(root, 'scripts/update-ens.sh'), [r.ensName, r.ipnsName, '--network', 'mainnet'], { stdio: 'inherit' });
    if (result.status !== 0) process.exit(result.status ?? 1);
    console.log('');
  }
  if (doUberEns && uberRow) {
    await requireConfirm(`Submit ENS contenthash transaction for ${uberRow.ensName} (root uber-UI)?`);
    const result = spawnSync(path.join(root, 'scripts/update-ens.sh'), [uberRow.ensName, uberRow.ipnsName, '--network', 'mainnet'], { stdio: 'inherit' });
    if (result.status !== 0) process.exit(result.status ?? 1);
    console.log('');
  }
}

console.log('\nTestnet naming inventory:');
for (const r of rows) console.log(`  ${r.slug}: https://${r.dnsName} -> /ipns/${r.ipnsName} (ENS contenthash: ${r.ensName}; nested eth.limo HTTPS is not currently operational — see workflow/deployment.md)`);
if (uberRow) console.log(`  uber-ui: /ipns/${uberRow.ipnsName} (ENS contenthash: ${uberRow.ensName}; intended URL: https://${uberRow.ensName.replace(/\.eth$/, '')}.eth.limo/testnet/commonality/#/)`);
