#!/usr/bin/env node
// Generate render.yaml from render.yaml.template + a deployment env file.
//
// Usage:
//   node scripts/generate-render-yaml.mjs [path/to/deployment.env]
//
// Default env file: deployments/base-sepolia.env
//
// The template uses two substitution patterns:
//   value: "${KEY}"       — required; errors if KEY is missing from env file
//   sync: false  # from-env: KEY  — optional; becomes value: "..." if KEY is
//                                   present, stays sync: false if not

import { readFile, writeFile } from 'node:fs/promises'
import { dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const rootDir = join(dirname(fileURLToPath(import.meta.url)), '..')
const envFile = process.argv[2] ?? join(rootDir, 'deployments', 'base-sepolia.env')
const templatePath = join(rootDir, 'render.yaml.template')
const outputPath = join(rootDir, 'render.yaml')

function parseEnvFile(content) {
  const env = {}
  for (const line of content.split('\n')) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const eq = trimmed.indexOf('=')
    if (eq === -1) continue
    const key = trimmed.slice(0, eq).trim()
    let value = trimmed.slice(eq + 1).trim()
    // Strip surrounding quotes if present
    if ((value.startsWith('"') && value.endsWith('"')) ||
        (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1)
    }
    env[key] = value
  }
  return env
}

// Escape a value for use inside a YAML double-quoted string.
function yamlDoubleQuoteEscape(value) {
  return value
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\n/g, '\\n')
    .replace(/\r/g, '\\r')
    .replace(/\t/g, '\\t')
}

function generate(template, env) {
  const errors = []

  // Replace required placeholders: value: "${KEY}"
  let output = template.replace(/value: "\$\{([^}]+)\}"/g, (match, key) => {
    if (!(key in env)) {
      errors.push(`Missing required key in env file: ${key}`)
      return match
    }
    return `value: "${yamlDoubleQuoteEscape(env[key])}"`
  })

  // Replace optional from-env entries: sync: false  # from-env: KEY
  output = output.replace(
    /sync: false\s+# from-env: (\S+)/g,
    (match, key) => {
      if (!(key in env)) return match
      return `value: "${yamlDoubleQuoteEscape(env[key])}"`
    }
  )

  if (errors.length > 0) {
    throw new Error(`Template substitution failed:\n${errors.map(e => `  - ${e}`).join('\n')}`)
  }

  return output
}

const [templateContent, envContent] = await Promise.all([
  readFile(templatePath, 'utf8'),
  readFile(envFile, 'utf8'),
])

const env = parseEnvFile(envContent)
const relativeEnvFile = relative(rootDir, envFile)

let output = generate(templateContent, env)

// Prepend a generated-file header
const header = `# Generated by scripts/generate-render-yaml.mjs from ${relativeEnvFile}\n# Do not edit directly — edit render.yaml.template instead.\n`
output = header + output.replace(/^# Render deployment.*\n# See.*\n#\n# THIS FILE IS A TEMPLATE.*\n#.*\n/, '')

await writeFile(outputPath, output, 'utf8')

const remaining = [...output.matchAll(/from-env:/g)].length
console.log(`Wrote render.yaml`)
console.log(`  Env file: ${relativeEnvFile} (${Object.keys(env).length} keys)`)
if (remaining > 0) {
  console.log(`  ${remaining} from-env entries not yet in env file (kept as sync: false) — re-run after deployment fills them in`)
}
